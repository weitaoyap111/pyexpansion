# type
# this will return type of data type

# bool
BoolType = type(bool())

# int
IntType = type(int())

# str
StrType = type(str())

# tuple
TupleType = type(tuple())

# type of list
ListType = type(list())
NumberListType = type(list[int])
StringListType = type(list[str])
ListOfListType = type(list[list])
DictionaryListType = type(list[dict])
TupleListType = type(list[tuple])

# dict
DictType = type(dict())

# none
NoneType = type(None)

# exception
ExceptionType = type(Exception())
